#[test]
    fn iterator_demonstration() {
        let v1 = vec![1, 2, 3];

        let mut v1_iter = v1.iter();

        assert_eq!(v1_iter.next(), Some(&1));
        assert_eq!(v1_iter.next(), Some(&2));
        assert_eq!(v1_iter.next(), Some(&3));
        assert_eq!(v1_iter.next(), None);
        //The iter method produces an iterator over immutable references. If we want to create an iterator that takes ownership of v1 and returns owned values, we can call into_iter instead of iter. 
        //Similarly, if we want to iterate over mutable references, we can call iter_mut instead of iter.
    }

    //Methods that call next are called consuming adaptors, because calling them uses up the iterator. 
    //One example is the sum method, which takes ownership of the iterator and iterates through the items by repeatedly calling next
#[test]
    fn iterator_sum() {
        let v1 = vec![1, 2, 3];

        let v1_iter = v1.iter();

        let total: i32 = v1_iter.sum(); //cant use iterator afterwards, its been consumed

        assert_eq!(total, 6);
    }

